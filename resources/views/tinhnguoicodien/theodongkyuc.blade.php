<p style="text-align: justify;">&nbsp;-Sáng 7/1/1973 Toàn tiểu đoàn d76f304b chuẩn bị đi B. (Đại tá Ngôn csv K4 vào c3 cùng Tôi) kể sau này thì toàn D76 đã kéo vào nam Tây nguyên chiến đấu, tiện mồm phũ luôn "Mày có vào đợt ấy thì toạch sớm, ko dính đạn thì sốt rét, đói thì cũng chết". Đợt này đồng môn Nhà văn Nguyễn Trọng Luân c2 vào Chiến trường, sau này phọt ra tiểu thuyết "Rừng đói" nổi tiếng. Tôi được chọn đi học lớp A trưởng tại Đoàn Huấn luyện Sư đoàn 304b, ko phải đi B. Sống nhăn răng để bây giờ mới kể
                    Đêm 27/1/1973 giao thừa sương trắng rừng núi, gác đêm rét chui vào bếp lửa chuồng lợn-sưởi trộm, dỏng tai nghe tiếng đài Ôriôntôn từ trên nhà Cbộ vẳng xuống-đọc toàn văn bản Hiệp định Pa ri vừa được ký kết, phấp phỏng hy vọng có ngày sẽ được về ĐHCĐ học tiếp. Tết lính được cặp bánh chưng ăn lúc nào thì ăn. Sau liên hoan đón Giao thừa toàn đại đội-là khoảnh khắc yên tĩnh của rừng núi -là giây phút nhớ nhà da diết trong tim của mọi thằng. Ngày này năm 1973 là mồng một tết Nhâm Tý
                    -Rạng sáng 28/1/1973 có trận chiến thư hùng tại cảng Cửa Việt chống lính SG nống ra giành đất cắm cờ (sau này Tôi mới biết, "Chứ giữa rừng gìa Tôi có biết gì đâu") Biết các quê LXT tham chiến trận Cửa Việt năm nao : Thị trưởng Cung, Thi sỹ Kẻ Mơ Thái, Trưởng ban 2G Thắng, quê Chính nữa có tụ họp rót và ôn lại kỷ niệm xưa.</p>
<p style="text-align: justify;">&nbsp;-Đại tá nhà văn Nguyễn Khắc Nguyệt nhắn tin "Trân trọng mời quê Lý Giàn...tới rót với VNS mặc áo lính", dự lễ ra sách "Vũ điệu Tango trên bãi biển Cửa Việt" của Nguyễn Khắc Nguyệt dày 387 trang (loại sách ko bán ra ngoài). Chả biên ra sợ "dài dòng văn tự”.</p>
<br>
<p style="text-align: justify;">&nbsp;Ngẫm thấy thời gian nhanh như chó chạy, trận Cửa Việt trôi qua 51 năm chằn chặn, còn nhiều trận chiến trong thời 21 năm oánh Mỹ sử  sách thì viết khô khan, truyện lính kể thực là sinh động vừa ít người viết, vừa ít xuất bản vì tỷ lý do nhạy cảm, những người tham gia các trận chiến đó già con mẹ nó rồi. Họ mà toạch là mất ty tỷ pho "truyện ngoài chính sử" quý báu. Thế nên sách của ngài Tư lệnh Fb LXT chôm được, quả là Lý trưởng lãi nhớn.</p>
<p style="text-align: justify;">&nbsp;-Hoạ sỹ Lê Trí Dũng đăng statut : Rằm tháng Chạp 28/1/2021 trưởng tràng lớp vẽ LXT năm xưa Trần Minh Hải đem sách được in bài viết, rượu biếu thày ! Gặp nhau giữa phố cổ Hà Thành-Phố Nguyễn Xí trong mưa phùn giá lạnh ngày giáp Tết thật thú vị! Bèn tặng lại học trò ba cuốn sách và cuốn tạp chí Văn nghệ Cà mau miền đất yêu dấu mình chưa từng đến !(ảnh cuối)</p>
<p style="text-align: justify;">&nbsp;-P/S ngày 28/1/2024 Hà nội đang chìm trong đợt mưa rét sun vòi 10-13 độ C. Tôi được Tiểu ban Di tích Kẻ Giàn Trung Kính Hạ mời ra Đình Trung Kính Hạ dự Lễ Thánh hoá và bầu người mở cửa Nghè Giao thừa năm nay</p>
