                        <div class="text-justify" style="line-height:23px" id="Art_Pdesc">
<p style="text-align: justify;">&nbsp;-K6 Đại học Cơ điện Bắc thái niên khoá 10/1970-9/1975. Biên chế ban đầu có 165 người của 3 lớp K6ma, K6mb, K6i (đỗ đại học kỳ thi đầu tiên của miền bắc xhcn tháng 7/1970) tụu trường ở nhờ Trung cấp Luyện kim, một tháng lên rừng xanh núi đỏ-tự khai thác nứa tranh ở rừng về xây dựng nhà ở và lớp học trên các quả đồi trọc xóm Cầu thông, xã Tích lương, huyện Đồng hỷ, tỉnh Bắc thái, thành những công dân đầu tiên của Bang K6 gần chợ T Ba nhất. Sơ bộ trong khoá học: tính từ 1/1971-9/1972 có 49 sinh viên đi bộ đội (11 Liệt sỹ-38 về học lại từ K8 đến K13). Ăn ở tập trung, nhảy tàu trốn vé xe lửa, trốn vé xem phim…hun đúc tình đồng môn ấm nồng trải dài qua năm tháng chiến tranh & bao cấp. Truyền thống giao lưu K6 đầu tháng 10 hàng năm-nưc tiếng toàn trưởng, Blog-Facebook K6 học sinh của trường ai cũng biết-thôi không nhắc lại nữa.
    Từ năm 2023 trở đi lớp cựu sinh viên K6 không còn ai U70 nữa, nghĩa là Thất thập cổ lai hy tất tần tật-Già hết cả lượt rồi-nhưng tình người Cơ điện thẳm sâu vưỡn như lửa cháy phừng phực. Từ thập kỷ 198x năm nào dân K6 cũng gặp nhau ít nhất một lần Phượt, một lần Tất niên & các hoạt động Hiếu Hỷ, ngày 27/7…Năm qua có hai đợt K6 đi Phượt lên rừng xuống biển, có hai vị về trời xanh bao la (1/6 khoá học đã vĩnh biệt chúng tôi) nhiều vị vì ốm đau đã thông qua zalo xin kiếu; có hai vị nghe nói bị K đang điều trị có triển vọng tốt.</p>
    <br>
<p style="text-align: justify;">&nbsp;Năm 2024 này kỷ niệm 54 năm ngày nhập trường, hôm nay 21/1/2024 là ngày đầu tiên của đợt gió mùa đông bắc (mưa phùn ướt áo và rét sun vòi khi xuống xe buýt) Ước tính có 1/6 người cựu sinh viên K6 đi ăn Tất niên tại nhà hàng Kim Kê quán thuộc Khu đô thị nhà giàu Time city. Kế thế để biết rằng hội K6 ĐHCĐ còn tên nào sống nhăn răng thì vẫn còn hội K6 họp thường niên & chén Tất niên…</p>
<br>
<p style="text-align: justify;">&nbsp;Mừng thay thấy các Kiều nữ K6 tròn 1 mâm vẫn xinh tươi eo tròn mát mắt. Cánh con trai tu hết can rượu sâm của con trai Hội trưởng Hồ Lê Thanh cung tiến. Mấy anh cán bộ lớp vẫn còn máu lắm, họ tham gia đều mọi hoạt động của K6 trong năm. Thủ quỹ Thuý Dần về già siêu nạc xinh ra, chọn món ăn ngon… Một số đồng môn ở xa “đánh giây thép” về hỏi han buổi Tất niên & tỏ ý tiếc không tham dự được vì lý do bất khả kháng,  nghe ấm lòng tình người bang T Ba nhất (ăn như Sư-khổ bỏ Sừ năm nao-Tự hào thành Triệu phú thời gian khi bước sang thế kỷ 21. Hôm nay K6 làm tất niên thời tiết không thuận nhưng cũng khá đông vui. Gặp nhau hôm nay có thêm các bạn cựu sinh viên K10 đồng thời là cựu chiến binh tham gia, rất cảm ơn các bạn. Muốn hô to một câu "Hàng năm còn gặp mặt được là lãi lắm rồi. Mọi người cố lên. Thánh họ". Trần Vũ Liệu có câu thơ hay rằng :</p>
<p style="text-align: justify;">&nbsp;Cũng là một bữa ăn thôi</p>
<p style="text-align: justify;">&nbsp;Nhưng mà ấm áp bồi hồi lòng nhau</p>
