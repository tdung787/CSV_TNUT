                    <p class="fw-bold">Cách đây 45 năm, ngày 25 tháng 10 năm 1974, những chàng trai cô gái từ nhiều&nbsp; vùng miền tổ quốc, đã rời quê, đến với mảnh đất Thái Nguyên đất thép, đến với mái trường Đại học Cơ điện, đó chính là khóa 10 của trường Đại học Cơ Điện, gọi tắt là K10 Cơ Điện, trong đó có các sinh viên khóa K10MB và MC</p>

                        <div class="text-justify" style="line-height:23px" id="Art_Pdesc">
                <p>Và cũng thời gian này, cách đây 40 năm, những chàng trai, cô gái của tập thể K10 đó, đã tốt nghiệp và rời mái trường thân yêu, tỏa về khắp mọi miền tổ quốc, mang tri thức đã được học để dựng xây đất nước, đã có biết bao những CSV K10 trở thành những nhà khoa học, nhà lãnh đạo, nhà kinh tế và đóng góp sức mình cho công cuộc xây dựng đất nước và làm rạng danh mái trường Đại học cơ điện mến yêu (nay là trường ĐHKT Công nghiệp anh hùng)</p>
<p>Ngày hôm nay, sau nhiều năm xa cách, những chàng trai cô gái đó, người đã thành ông, thành bà, mái tóc đã điểm sương, người còn, người mất, họ lại tụ lại về đây, về với Thái Nguyên đất thép, về với chợ Tê ba nhất, về với Tháp bút cơ điện, về với nhà A3, về với thầy và trò trường Đại học Kỹ thuật Công nghiệp – Đại học Thái Nguyên để gặp lại nhau, ôn lại những kỷ niệm vui, buồn của một thời sinh viên sôi nổi, để thăm lại mái trường xưa và để thấy được sự đổi thay của mái trường sau nhiều năm xa cách.</p>
