            <b style="font-size: larger">K10 CƠ ĐIỆN – NGÀY ẤY VÀ BÂY GIỜ 1974 - 2019</b>

                        <div class="text-justify" style="line-height:23px" id="Art_Pdesc">
                <p style="text-align: justify;">&nbsp;Ngày này cách đây đúng 45 năm, ngày 25 tháng 10 năm 1974, những chàng trai trẻ &nbsp;từ nhiều tỉnh miền Bắc, tụ về một điểm gọi là Tê Ba nhất, Thái Nguyên, lập nên khóa 10 của trường Đại học Cơ Điện, gọi tắt là K10 Cơ Điện (sau này K10 còn có đợt bổ sung ngày 15/11/1974 và sau giải phóng miền Nam còn vinh dự được đón các anh sinh viên K6 vừa gác súng trở về từ chiến trường miền Nam tiếp tục sự nghiệp bút nghiên).</p>
<p style="text-align: justify;">&nbsp; &nbsp; &nbsp; &nbsp; Sau 5 năm học tập chúng ta đã trở thành những kỹ sư Cơ Điện trẻ trung sẵn sàng tung bay đi khắp các miền xây dựng Tổ quốc, nhưng rồi do biến động lịch sử, chúng ta lại trải qua 3 tháng đào tạo sỹ quan dự bị (SQDB) tại trường Sỹ quan Thiết giáp, rồi các Kỹ sư, Sỹ quan trẻ ấy mới lên đường xây dựng và bảo vệ Đất nước.</p>
<p style="text-align: justify;">&nbsp; &nbsp; &nbsp; &nbsp; Trải qua những năm tháng thăng trầm cùng đất nước, chúng ta đã dần trưởng thành, người ở lại quân đội cũng tự hào với thành tích tham gia bảo vệ Tổ quốc, người ra dân sự cũng tự hào với những đóng góp nhỏ bé của mình cho nền kinh tế nước nhà.<br>Hàng năm, anh em K10 cũng vẫn lẻ tẻ kéo nhau lên trường, gặp gỡ nhau nhân ngày Truyền thống Cơ Điện 6/12 do nhà trường tổ chức.</p>
<p style="text-align: justify;">&nbsp; &nbsp; &nbsp; &nbsp; Năm 2009, nhân dịp kỷ niệm 35 năm nhập trường, 30 năm tốt nghiệp..., từ khởi xướng của một nhóm nhỏ, được sự ủng hộ nhiệt tình của đông đảo anh em đồng môn, gần như toàn bộ khóa 10 đã tập trung đông đủ, tạo nên hai ngày lễ lịch sử đầu tiên của K10 Cơ Điện: Ngày ấy &amp; Bây giờ 2009.</p>
<p style="text-align: justify;">&nbsp; &nbsp; &nbsp; &nbsp; Từ đấy, hàng năm K10 Cơ Điện duy trì tổ chức ngày hội K10 Cơ Điện của mình, đặc biệt ấn tượng là:</p>
<p style="text-align: justify;">&nbsp; &nbsp; &nbsp; &nbsp; Ngày hội K10 Cơ Điện ngày ấy &amp; Bây giờ 2013 tái lập lại cảnh sinh hoạt ở T Ba nhất xưa.</p>
<p style="text-align: justify;"><img src="https://tnut.edu.vn/uploads/tinyfiles/uploads/files/cuusinhvien/1.png" alt="1.png" width="100%"><img src="https://tnut.edu.vn/uploads/tinyfiles/uploads/files/cuusinhvien/2.png" alt="2.png" width="100%"></p>
<p style="text-align: justify;"><img src="https://tnut.edu.vn/uploads/tinyfiles/uploads/files/cuusinhvien/3.png" alt="3.png" width="100%"><img src="https://tnut.edu.vn/uploads/tinyfiles/uploads/files/cuusinhvien/4.png" alt="4.png" width="100%"></p>
<p style="text-align: justify;">&nbsp;Ngày hội K10 Cơ Điện 2014, nhân kỷ niệm 40 năm nhập trường, 35 năm tốt nghiệp, tái lập lại cảnh đón sinh viên nhập trường, dàn dựng lễ khai giảng 1974 và bế giảng 1979 với những sự kiện nhỏ gợi nhớ lại không gian xưa.</p>
<p style="text-align: justify;">&nbsp; &nbsp; &nbsp; &nbsp; Rồi đến năm 2017, nhân dịp kỷ niệm tròn 60 tuổi của các thành viên K10, chúng ta lại cùng nhau tổ chức lễ hội Lục thập hoa giáp của những con gà Đinh Dậu, một dấu ấn của K10 với kỷ niệm chương Lục thập hoa giáp cho tất cả các thành viên tham gia ngày hội.</p>
<p style="text-align: justify;">&nbsp; &nbsp; &nbsp; &nbsp; Trong mười năm qua, chúng ta đã tạo nên những sự kiện mang dấu ấn mà cho đến nay, ít có khóa nào làm được:</p>
<p style="text-align: justify;">&nbsp; &nbsp; &nbsp; &nbsp; Hai quyển sách kỷ yếu K10 Cơ Điện Ngày ấy &amp; Bây giờ 2009 và 2014</p>
<p style="text-align: justify;">&nbsp; &nbsp; &nbsp; &nbsp; Những chiếc huy hiệu K10, mũi tên T ba nhất lịch sử, bản sao Giấy chứng nhận học sinh, Bằng Tốt nghiệp đại học, đĩa sứ kỷ niệm Cơ Điện, lô gô ghép hình huy hiệu K10 Cơ Điện, kỷ niệm chương Lục thập hoa giáp ...</p>
<p style="text-align: justify;"><img src="https://tnut.edu.vn/uploads/tinyfiles/uploads/files/cuusinhvien/5.png" alt="5.png" width="100%"><img src="https://tnut.edu.vn/uploads/tinyfiles/uploads/files/cuusinhvien/6.png" alt="6.png" width="100%"></p>
<p style="text-align: justify;">&nbsp;Đặc biệt, sự tham gia tích cực của K10 Cơ Điện với vai trò chủ lực trong hai sự kiện chính trên trường là:</p>
<p style="text-align: justify;">&nbsp; &nbsp; &nbsp; &nbsp; Tham gia đêm giao lưu giữa thế hệ Cơ Điện cùng thế hệ Công nghiệp nhân dịp kỷ niệm 50 năm thành lập trường, khơi lại niệm tự hào truyền thống Cơ Điện trong lòng thế hệ sinh viên hiện tại.</p>
<p style="text-align: justify;">&nbsp; &nbsp; &nbsp; &nbsp; Tổ chức vận động và xây dựng Tháp bút Cơ Điện, là quà tặng của thế hệ Cơ Điện cho nhà trường và các thế hệ đàn em. Một tượng đài mang nhiều ý nghĩa lịch sử và giáo dục truyền thống của nhà trường.</p>
<p style="text-align: justify;"><img src="https://tnut.edu.vn/uploads/tinyfiles/uploads/files/cuusinhvien/7.png" alt="7.png" width="100%"></p>
<p style="text-align: justify;">&nbsp; &nbsp; &nbsp; &nbsp;Với những dấu ấn của K10 Cơ Điện cho nhà trường, có lẽ không thể không kể đến một đóng góp cho nhà trường mà K10 có quyền tự hào, đó chính là việc nhiều cựu sinh viên của K10 Cơ Điện, sau khi tốt nghiệp đã được giữ lại trường làm giảng viên truyền lại kiến thức cho các thế hệ đàn em, đặc biệt trong đó có bạn Phan Quang Thế, sau 30 năm phấn đấu, đã trở thành Hiệu trưởng nhà trường</p>
<p style="text-align: justify;"><img src="https://tnut.edu.vn/uploads/tinyfiles/uploads/files/cuusinhvien/8.png" alt="8.png" width="100%"></p>
<p>&nbsp; Và gần đây chúng ta tiếp tục ủng hộ việc xây dựng tượng đài kỷ niệm Thầy trò Cơ Điện gác bút nghiên lên đường, cầm súng bảo vệ Tổ quốc.</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; Hôm nay, chúng ta ngồi đây với nhau, nhớ lại lịch sử K10 Cơ Điện, điểm lại những điểm nhấn của K10 kiêu hùng trong những năm qua, tự hào với những gì K10 đã làm được.</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; Vì nhiều lý do khách quan và chủ quan, lần kỷ niệm này, chúng ta không làm lớn như năm 2009 và 2014, nhưng những gì K10 Cơ Điện cho đến nay, vẫn là những dấu ấn và những sự kiện lớn và có ý nghĩa với bản thân chúng ta và cả với mái trường thân yêu xưa, trường Đại học Cơ Điện của chúng ta.</p>
<p>&nbsp;&nbsp; &nbsp; &nbsp; &nbsp; Xin cảm ơn những người có mặt trong lễ kỷ niệm K10 Cơ Điện 45 năm nhập trường, 40 năm tốt nghiệp, cả những người anh em Cơ Điện khác khóa như K6, K7, K8..., K11, K12, và cả những người phụ nữ " không phải Cơ Điện nhưng vẫn yêu quý Cơ Điện", họ là những người đã góp phần tạo nên danh hiệu K10 kiêu hùng và vẫn luôn luôn yêu mến, thủy chung với danh hiệu K10 kiêu hùng.</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; Bồi hồi nhớ lại những năm tháng K10 KIÊU HÙNG.</p>
