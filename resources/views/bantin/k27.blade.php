            <b style="font-size: larger">SINH VIÊN KHÓA K27 - KÝ ỨC MỘT THỜI</b>
                    <p class="fw-bold">          Có lẽ với ai cũng vậy, những tháng ngày sinh viên là quãng thời gian đẹp nhất, và dù quãng thời gian ấy có nhiều khó khăn, vất vả và những âu lo nhưng ta đã sống với trọn vẹn tuổi trẻ của mình, chẳng hề phải nuối tiếc. Kỷ niệm về những năm tháng tươi đẹp của tuổi trẻ sẽ vẫn mãi vẹn nguyên trong tim mỗi chúng ta. Mặc dù thời gian học cấp 3 (gọi theo cách thời xưa từ đầu những năm 80 thế kỷ 20 trở về trước) là khoảng thời gian đi học vui nhất, có nhiều kỉ niệm với nhau nhất bởi vì đó là cũng là thời gian dài được học chung, chơi chung suốt cả ba năm học, tuy nhiên đến với giảng đường đại học, lại là những niềm vui khác, kỉ niệm khác, nhiều thứ rất khác, đặc biệt là trong giai đoạn 1991-1996.</p>

                        <div class="text-justify" style="line-height:23px" id="Art_Pdesc">
                <p style="text-align: justify;">&nbsp;Chắc ai qua cái thời sinh viên rồi cũng vậy, sẽ có những lúc nhớ lại cái thời sinh viên của mình thật da diết, nhớ bạn bè và thầy cô đến nao lòng. Nhớ ngôi trường ấy, nơi đã in dấu cả một thời tuổi trẻ, nhiều khi bao nhiêu cảm xúc cứ thế lại ùa về, nguyên vẹn như vừa mới hôm qua. Nhớ từng dãy nhà, nhớ từng bậc hành lang, nhớ cả những hàng cây xà cừ già và những lối đi quen thuộc.</p>
<p style="text-align: justify;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Ngày đó trường Đại học Kỹ thuật công nghiệp Thái Nguyên (nay là Đại học Kỹ thuật Công nghiệp – Đại học Thái Nguyên) là một trường độc lập riêng rẽ. Khóa K27 (niên khóa 1991- 1996) ban đầu bao gồm 154 thành viên (trong đó có 10 nữ) đến từ 18 tỉnh thành phía Bắc từ Hà Tĩnh trở ra được phân làm 03 lớp gồm lớp K27A với 55 thành viên, lớp K27B với 48 thành viên, lớp K27C với 51 thành viên</p>
<p style="text-align: justify;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Đại học Kỹ thuật công nghiệp Thái Nguyên, nơi có những kỉ niệm không thể nào quên, của những ngày tháng chắc chắn sẽ không thể có ở bất kì nơi nào khác; đó là ngày đầu nhập học bỡ ngỡ, mọi thứ còn xa lạ; là nỗi nhớ nhà của những đứa sinh viên ở huyện hay ngoại tỉnh; là những buổi chiều tập trung ngồi chờ bên bờ ao để đến giờ nhận thư của bạn bè, người thân hay là nhận giấy gửi tiền từ quê lên, lúc đó đến điện thoại cố định cũng là một thứ rất xa xỉ nói gì đến khái niệm điện thoại di động mà cho đến tận khi ra trường cũng làm gì có, những buổi tối kéo nhau kẹp 2, 3 người cùng trên những xe đạp cà tàng mà thời đó cứ gọi là xe chống sét (còn trêu nhau là cấm đi qua khu gang thép Thái Nguyên để không bị tịch thu nấu sắt vụn) để đi các trường khác chơi như đại học y Thái Nguyên, đại học sư phạm Thái Nguyên hay đại học Nông nghiệp, mà đa phần là tập trung tại địa điểm trường sư phạm, mùa nóng thì cởi trần đạp xe trên đường, lên tới nơi đứng chờ khô ráo mồ hôi mới mặc áo để vào phòng các bạn nữ ngồi chơi, cũng lạ thiệt, thời đó đến chơi chỉ toàn ngồi lì trong phòng nói chuyện cho đến khi về (có cặp nào yêu thì cơ bản cũng vậy thôi), mà đồ dùng trên người đa phần cũng là mượn của nhau hoặc dùng chung. Không phải riêng khóa 27 đa phần là sinh viên nam mà cả trường thời đó toàn vậy, cả khóa chỉ có 10/154 là nữ, ngày bình thường thì cũng có những khi lười học lắm nhưng vào những lúc ôn thi hay làm bài tập lớn, đồ án… thì cứ thâu đêm suốt là bình thường, hình như kiểu học đấy như như một thương hiệu sinh viên công nghiệp thời bấy giờ và cũng cho đấy là điều bình thường, rồi những ngày tháng học quân sự tại Trung tâm Giáo dục quốc phòng ở trường cao đẳng sư phạm Thái Nguyên ở trong Thịnh Đán, những lúc lân la quán xá, trà đá mở rộng,… hay có cả những mối tình sinh viên đẹp đẽ nào đó. Thật quá nhiều điều để nhớ!</p>
<p style="text-align: justify;"><img src="https://tnut.edu.vn/uploads/tinyfiles/uploads/files/cuusinhvien/k27._thi_sinh_vien.1.jpg" alt="K27._THI_SINH_VIEN.1.jpg" width="100%"></p>
<p style="text-align: justify;">&nbsp;&nbsp; &nbsp;Điều kiện cuộc sống vật chất của sinh viên thời đó thì cũng khó khăn lắm, học giỏi và có ưu tiên thì loại 1 cũng 5, 6 chục ngàn đồng/tháng, dân tỉnh thì chủ yếu, mà ở quê thì nhà nào chẳng khó khăn, ngày mới vào thì còn quy định không phải đóng học phí, ăn cơm nhà bếp thì mua vé 700 đồng/suất còn ăn ở bên ngoài thì 800 đồng/suất (những quán cơm như chị Hồng, chị Cúc…), trà đá đường thì 200 đồng/cốc, nhiều người sáng ra còn không dám đi thể dục vì sợ như thế đói quá không chịu được, còn nhớ năm 1991 lúc năm thứ nhất, những buổi cùng học chung tại hội trường lớn cứ đến 4/6 tiết là trốn đi ăn cơm, hết tiết 6 lại tiếp tục đi ăn, suất cơm thì cũng ít lắm, thức ăn chỉ vài miếng thịt thủ mỏng dính, mà ngày đó sao thấy ăn ngon thế, lại còn điều này nữa, trai công nghiệp thời đó cũng oai danh lắm (so với trai sinh viên của trường y và trường nông nghiệp) vì thế mà được các bạn gái ở sư phạm rất quý mến, thậm chí nảy nở tình yêu cũng rất nhiều, mặc dù khó khăn về kinh tế nhưng lúc nào cũng tỏ ra ga lăng, mà đúng thế thật mặc dù sau đó thì méo mặt vì thiếu tiền, cũng có những vụ hài hước thế này, chẳng là thường sáng thứ 7 hàng tuần các bạn nữ trên trường sư phạm thường đi tàu hỏa xuống trường công nghiệp để chơi (nhiều bạn công nghiệp có lúc gọi đùa là thanh tra kinh tế), hơn 7 giờ sáng là đi bộ vào tới trường rồi, vẫn nằm ở trên giường nhà ký túc xá ngó xuống điểm danh xem có bạn của mình không, nếu còn chút tiền hoặc vẫn còn nợ quán được thì chất ga lăng nổi lên ngay, nào là tiếp đón, dẫn đi chơi, đi uống nước, ăn bữa trưa, bữa chiều, rồi tối lại mượn xe cà tàng tổ chức đưa nhau về, cũng vui và tình cảm lắm, còn không được như thế thì đành tủi thân im lặng mà trốn mặt thôi, coi như vắng ở ký túc xá, cái này cũng là điều xảy ra thường xuyên, lặp lại nhiều tuần thậm chí nhiều năm, chắc chỉ có những ai thời đó mới thấu hiểu hết tình cảnh đó. Thời đó khổ nhưng thực sự ai cũng có tố chất và nền tảng rất tốt, rất nhiều người học ở trường chuyên, lớp chọn, những người đã từng thi học sinh giỏi cấp huyện, cấp tỉnh, bên cạnh đó thì ai lo lắng cho việc học hành, thương bố mẹ ở quê nhà lam lũ vất vả, và cứ như thế, vừa ồn ào, sôi nổi lại vừa êm đềm đi qua với bao điều còn đọng lại.</p>
<p style="text-align: justify;"><img src="https://tnut.edu.vn/uploads/tinyfiles/uploads/files/cuusinhvien/k27._1991.jpg" alt="k27._1991.jpg" width="100%"></p>
<p style="text-align: center;">&nbsp; &nbsp; <em>&nbsp; &nbsp; &nbsp;Các thế hệ Hiệu trưởng thời đó là thầy Lê Cao Thăng (ban đầu) và thầy thầy Võ Quang Lạp (về sau khi tốt nghiệp).</em></p>
<p style="text-align: justify;">&nbsp;</p>
<p style="text-align: justify;">&nbsp; Chương trình học được chia làm 2 giai đoạn: Giai đoạn 1 học cơ sở, giai đoạn 2 học chuyên ngành, tuy giai đoạn đầu được chia làm 3 lớp nhưng vẫn học chung cùng nhau rất nhiều môn (Toán, lý, triết, kinh tế chính trị…). Thời kỳ này mặc dù đã sau đổi mới được 05 năm nhưng đời sống kinh tế còn cực kỳ khó khăn, nhà trường lúc đó cơ bản chia ra làm 03 khu: Khu A là giảng đường, ký túc xá, nhà làm việc và thư viện, khu B là khu hành chính kết hợp với nhà ở giáo viên và khu C là khu xưởng; riêng khu A khi đó có 3 dãy nhà 3 tầng A1, A2, A3 dùng làm giảng đường, 04 nhà 5 tầng gồm nhà A4 là khu hiệu bộ, từ A5 đến A7 là ký túc xá, riêng nữ ở ký túc xá chỉ có 1 /2 tầng 1 nhà A7. Khi bắt đầu mới vào các khu nhà 05 tầng ở khu A còn chưa được sửa chữa, toàn bộ khu ký túc xá nhà vệ sinh không sử dụng được, một số sinh viên còn lấy đó làm phòng ở, một số phòng ký túc xá còn được thông và ngăn bằng vách cót để tận dụng làm lớp học, đến năm 1993 và 1994 ký túc xá mới được cải tạo dần và cho sinh viên ở, ngày đó đa phần sinh viên ở ngoại tỉnh đều ở ký túc xá, chỉ một số thành phần có nhà gần trường mới không ở, tuy nhiên mối quan hệ rất gắn kết, không phân biệt nội trú và ngoại trú.</p>
<p style="text-align: justify;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Giai đoạn 1 học cơ bản với tổng số 126 trình, sinh viên đạt 90% tích lũy các môn học từ trung bình trở lên và các môn còn lại đều phải qua điểm liệt (điểm 3), sinh viên thời đó đầu vào cơ bản là rất chất lượng, tuy nhiên bên cạnh những sinh viên chăm học, những sinh viên rất thông minh thì cũng còn những sinh viên còn chưa tập trung hết mức cho việc học, với lại thời đó thi cử cũng rất nghiêm túc, các thầy cô đánh giá đúng thực chất. Kết quả là có một số sinh viên đã không thể đủ điều kiện để bước vào cuộc thi chuyển giai đoạn cực kỳ khốc liệt tiếp theo.</p>
<p style="text-align: justify;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Quá trình thi chuyển giai đoạn, hay gọi đúng nghĩa tại thời điểm đó là vượt rào, lại một lần thử thách ghê gớm, kết quả là một số bạn đã không thể vượt qua, số sinh viên không đủ điều kiện thi chuyển giai đoạn cũng như thi vượi rào không qua sau đó một số không học ở trường nữa, chuyển trường khác hoặc về địa phương, một số học lại cùng các khóa sau (số này cũng trên 20 người)</p>
<p style="text-align: justify;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Giai đoạn 2 là học chuyên ngành, khi đó chỉ có 2 khoa là khoa điện với chuyên ngành điện khí hóa xí nghiệp và khoa máy là chế tạo máy, khi đó khóa 27 được chia thành 3 lớp riêng biệt, 02 lớp điện là K27Ia (thầy Pha là GVCN) và K27Ib (cô Sơn là GVCN), 01 lớp cơ là K27M (cô vinh là GVCN)</p>
<p style="text-align: justify;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Thế rồi ngày ra trường cũng đến, buổi bảo vệ đồ án tốt nghiệp mặc dù không còn đốt pháo như xưa (đến năm 1994 mới cấm đốt pháo) nhưng vẫn đủ đầy trọn vẹn với thầy cô, bạn bè, hoa tươi, những lời chức mừng và cả những tự hào, kỳ vọng cho một tương lai tốt đẹp của bản thân.</p>
<p style="text-align: justify;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Con đường mà chúng tôi đã đi ngày hôm qua sẽ mãi chỉ còn trong kí ức, bước đi của thời gian chẳng chờ đợi một ai, cũng có nhiều người sẽ không còn về lại nơi này nữa, nơi đã gắn bó những năm tháng tuổi trẻ của họ, nơi đã chứng kiến sự thay đồi và trưởng thành trong cách suy nghĩ của họ. Sẽ chẳng gặp lại rằng xà cừ già trên những lối đi, sẽ chẳng còn đi về trên những con đường quen thuộc thủa nào… Tất cả, từ ngày mai sẽ được cất vào một nơi gọi là quá khứ, một góc gọi là kỉ niệm và chỉ còn được nhắc đến khi người ta kể về chuyện của ngày xưa.</p>
