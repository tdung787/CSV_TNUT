<p style="text-align: justify;">Thầm cảm ơn những năm tháng tại trường Đại học Kỹ thuật công nghiệp &nbsp;Thái Nguyên này, cảm ơn tất cả những gì đã có, đã mang lại cho chúng tôi ở quãng đời sinh viên. Một phần tuổi trẻ của chúng tôi đã và đang gắn với ngôi trường đại học này; có lẽ sẽ chẳng bao giờ kể hết những kỉ niệm đã có, những nỗi niềm muốn gửi gắm cho nhau. Chỉ ước rằng, thời gian sẽ trôi chầm chậm thôi, để chúng tôi có thể níu giữ những ngày cuối cùng được bên nhau, để nơi này sẽ thân thuộc thêm những ngày ngắn ngủi nữa. Có thể cuộc sống còn nhiều vất vả với những lo toan bộn bề, nhưng tôi tin kỉ niệm về những năm tháng tươi đẹp của tuổi trẻ sẽ còn đọng mãi trong tim mỗi chúng ta, những ai đã đi qua những tháng ngày sinh viên với những nốt buồn vui trầm bổng. Rồi ngày mai, khó có thể có được những lần bên nhau như đã có nữa. Xa nhau rồi, sẽ nhớ nhau lắm! Phải không?</p>
